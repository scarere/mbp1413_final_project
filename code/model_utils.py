class Attention_block(nn.Module):
    def __init__(self,F_g,F_l,F_int,attn_type='cosine'):
        super(Attention_block,self).__init__()
        self.attn_type = attn_type
        self.F_int = F_int
        self.F_l = F_l
        self.F_g = F_g
        self.W_g = nn.Sequential(
            nn.Conv2d(F_g, F_int, kernel_size=1,stride=1,padding=0,bias=True),
            nn.BatchNorm2d(F_int)
            )
        
        self.W_x = nn.Sequential(
            nn.Conv2d(F_l, F_int, kernel_size=1,stride=1,padding=0,bias=True),
            nn.BatchNorm2d(F_int)
        )
        
        if attn_type == 'cosine':

          self.psi = nn.Sequential(
              nn.Conv2d(F_int, F_l, kernel_size=1,stride=1,padding=0,bias=True),
              nn.BatchNorm2d(F_l)
          )
        
        elif attn_type == 'regular_full':

          self.psi = nn.Sequential(
              nn.Conv2d(F_int, F_l, kernel_size=1,stride=1,padding=0,bias=True),
              nn.BatchNorm2d(F_l)
          )
        
        elif attn_type == 'regular_full_dim_add':

          self.psi = nn.Sequential(
              nn.Conv2d(F_int, F_l, kernel_size=1,stride=1,padding=0,bias=True),
              nn.BatchNorm2d(F_l)
          )
          
        elif attn_type == 'channel_attention':

          self.psi = nn.Sequential(
              nn.Conv2d(F_int, F_l, kernel_size=1,stride=1,padding=0,bias=True),
              nn.BatchNorm2d(F_l)
          )
        
        else:

          self.psi = nn.Sequential(
              nn.Conv2d(F_int, 1, kernel_size=1,stride=1,padding=0,bias=True),
              nn.BatchNorm2d(1)
          )
        
        self.relu = nn.ReLU(inplace=True)
        
    def channel_mul(self, a, b):
        C = a.shape[1]
        cos = torch.from_numpy(np.cos(np.arange(C)*1.57/C).reshape(1,C,1,1)).float().cuda()
        sin = torch.from_numpy(np.sin(np.arange(C)*1.57/C).reshape(1,C,1,1)).float().cuda()
        return ((a*cos)*torch.unsqueeze((b*cos).sum(1),1)) + ((a*sin)*torch.unsqueeze((b*sin).sum(1),1))
        
    def forward(self,g,x):
        if self.attn_type == 'channel_attention':
            g1 = torch.cat((torch.nn.AvgPool2d(g.shape[2])(g),torch.nn.MaxPool2d(g.shape[2])(g)),dim=0) # torch.nn.AvgPool2d(g.shape[2])(g) + torch.nn.MaxPool2d(g.shape[2])(g)
            x1 = torch.cat((torch.nn.AvgPool2d(x.shape[2])(x),torch.nn.MaxPool2d(x.shape[2])(x)),dim=0) # torch.nn.AvgPool2d(x.shape[2])(x) + torch.nn.MaxPool2d(x.shape[2])(x)
            g1 = self.W_g(g1)
            x1 = self.W_x(x1)
            psi = self.relu(x1+g1)
            psi = torch.sum(torch.sigmoid(self.psi(psi)),axis=0) # torch.sigmoid(self.psi(psi))
            return x*psi
        else:
            g1 = self.W_g(g)
            x1 = self.W_x(x)
            psi = self.relu(x1+g1)
            psi = torch.sigmoid(self.psi(psi))

            if self.attn_type == 'cosine':

              return self.channel_mul(x,psi)
            
            elif self.attn_type == 'regular_full':

              return x*psi
            
            elif self.attn_type == 'regular_full_dim_add':

              return x*torch.unsqueeze(psi.sum(1),1)
            
            else:

              return x*psi


class U_Netv2(nn.Module):
    def __init__(self,img_ch=3,output_ch=1):
        super(U_Net,self).__init__()
        
        self.Maxpool = nn.MaxPool2d(kernel_size=2,stride=2)

        self.Conv1 = conv_block(ch_in=img_ch,ch_out=64)
        self.Conv2 = conv_block(ch_in=64,ch_out=128)
        self.Conv3 = conv_block(ch_in=128,ch_out=256)
        self.Conv4 = conv_block(ch_in=256,ch_out=512)
        self.Conv5 = conv_block(ch_in=512,ch_out=1024)

        self.Up5 = up_conv(ch_in=1024,ch_out=512)
        self.Up_conv5 = conv_block(ch_in=1024, ch_out=512)

        self.Up4 = up_conv(ch_in=512,ch_out=256)
        self.Up_conv4 = conv_block(ch_in=512, ch_out=256)
        
        self.Up3 = up_conv(ch_in=256,ch_out=128)
        self.Up_conv3 = conv_block(ch_in=256, ch_out=128)
        
        self.Up2 = up_conv(ch_in=128,ch_out=64)
        self.Up_conv2 = conv_block(ch_in=128, ch_out=64)

        self.Conv_1x1 = nn.Conv2d(64,output_ch,kernel_size=1,stride=1,padding=0)


    def forward(self,x):
        # encoding path
        x1 = self.Conv1(x)

        x2 = self.Maxpool(x1)
        x2 = self.Conv2(x2)
        
        x3 = self.Maxpool(x2)
        x3 = self.Conv3(x3)

        x4 = self.Maxpool(x3)
        x4 = self.Conv4(x4)

        x5 = self.Maxpool(x4)
        x5 = self.Conv5(x5)

        # decoding + concat path
        d5 = self.Up5(x5)
        d5 = torch.cat((x4,d5),dim=1)
        
        d5 = self.Up_conv5(d5)
        
        d4 = self.Up4(d5)
        d4 = torch.cat((x3,d4),dim=1)
        d4 = self.Up_conv4(d4)

        d3 = self.Up3(d4)
        d3 = torch.cat((x2,d3),dim=1)
        d3 = self.Up_conv3(d3)

        d2 = self.Up2(d3)
        d2 = torch.cat((x1,d2),dim=1)
        d2 = self.Up_conv2(d2)

        d1 = self.Conv_1x1(d2)

        return torch.sigmoid(d1)


